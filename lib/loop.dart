void main(){
  // goals kita ingin menampilkan 1-100
  for(int i = 0; i <= 5; i++){
    print(i);
  }


  List<String> days = ['bangun', 'makan', 'minum', 'belajar', 'salot', 'mati deh'];
  for (int i = 0; i < days. length; i++) {
    print(days[i]);
  }


  int number = 1;
  while (number <= 5) {
    print(number);
    number++;
  }
}